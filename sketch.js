function setup() {
    createCanvas(1080, 1000);
    background(204,204,255);    
}

function draw() {
    strokeWeight(0)
    fill(0,0,255)
    ellipse(300, 100, 100, 100)
    fill(255,255,204)
    ellipse(400, 500, 100, 100)
    strokeWeight(4)
    stroke(255,204,229)
    ellipse(350, 300, 100, 100)
    ellipse(100, 400, 100, 100)
    ellipse(700, 200, 100, 100)
    rect(300, 100, 50, 50)
}
